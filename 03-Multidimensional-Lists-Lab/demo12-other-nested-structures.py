# Sets in Lists, Lists and Sets as Dictionary Values

# We can also have sets inside of lists
sets_of_numbers = [
    {1, 2, 3},
    {3, 4, 5}
]

# or tuples in lists

tuples_collection = [
    ('peter', 'marry'),
    (22, 19)
]

# We can also have lists as dictionary values

students_and_grades = {
    'peter': [4.50, 5.00, 4.95],
    'anna': [6.00, 5.65, 5.80]
}

# or tuples as dictionary values

words_and_characters = {
    "bob":  ("b", "o", "b"),
    "anna": ("a", "n", "n", "a")
}
