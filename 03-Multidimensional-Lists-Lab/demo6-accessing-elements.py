# To access an element in a two-dimensional list for example,
# you should give the row and the column of the element

matrix = [
    [1, 2],
    [3, 4],
    [5, 6]
]
print(matrix[1][0])

# Example with 3 dimensional list

matrix = [
    [[1, 2], [3, 4]],
    [[5, 6], [7, 8]]
]
print(matrix[0][1][1]) # 4


